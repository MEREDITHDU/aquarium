#include <avr/io.h>
#include "uart.h"
#include "spi.h"

/* ------------------------------------------------------------------------- */
/* ----------------		 Program glowny 	    ---------------- */
/* ------------------------------------------------------------------------- */

int main(void)
{
  char c;
  UART_init();
  SPI_init();
  while(1)
  {
    c=UART_getchar();
    SPI_transfer_byte(c);
  }
}

